def test_init_ui_assets_helpers_registers_page_config_template_global():
    ui_assets = _import_ui_assets_module()

    class FakeApp:
        def __init__(self):
            self.static_folder = str(STATIC_DIR)
            self.extensions = {}
            self.template_globals = {}

        def add_template_global(self, func, *, name=None):
            self.template_globals[name or getattr(func, '__name__', '')] = func

    app = FakeApp()
    helper = ui_assets.init_ui_assets_helpers(app)

    assert isinstance(helper, ui_assets.FrontendAssetHelper)
    assert app.extensions["xkeen_ui_assets"] is helper

    entry_global = app.template_globals["frontend_page_entry_url"]
    page_config_global = app.template_globals["frontend_page_config"]

    assert getattr(entry_global, "__self__", None) is helper
    assert getattr(entry_global, "__func__", None) is getattr(ui_assets.FrontendAssetHelper, "frontend_page_entry_url")
    assert getattr(page_config_global, "__self__", None) is helper
    assert getattr(page_config_global, "__func__", None) is getattr(ui_assets.FrontendAssetHelper, "frontend_page_config")
