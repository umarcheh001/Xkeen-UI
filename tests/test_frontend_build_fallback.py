def test_frontend_asset_helper_requires_build_entries_in_build_only_mode(tmp_path):
    ui_assets = _import_ui_assets_module()
    previous = os.environ.get("XKEEN_UI_FRONTEND_SOURCE_FALLBACK")
    os.environ["XKEEN_UI_FRONTEND_SOURCE_FALLBACK"] = "0"

    try:
        static_dir = tmp_path / "static"
        helper = ui_assets.FrontendAssetHelper(static_folder=str(static_dir))
        resolution = helper.get_build_bridge_resolution("panel")

        assert resolution.build_enabled is True
        assert resolution.build_filename is None
        assert resolution.build_exists is False
        assert resolution.fallback_reason == "missing_build_entry"
        assert resolution.source_fallback_enabled is False
        assert not resolution.should_use_build
        assert not resolution.should_use_source_fallback

        try:
            helper.resolve_frontend_page_entry_filename("panel")
        except RuntimeError as exc:
            message = str(exc)
        else:
            raise AssertionError("build-only mode must not silently fall back to source entrypoints")

        assert "Frontend build-only mode requires a valid build entry" in message
        assert "missing_build_entry" in message
    finally:
        if previous is None:
            os.environ.pop("XKEEN_UI_FRONTEND_SOURCE_FALLBACK", None)
        else:
            os.environ["XKEEN_UI_FRONTEND_SOURCE_FALLBACK"] = previous


